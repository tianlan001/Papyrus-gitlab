package org.eclipse.papyrus.uml.profile.tests;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotates a JUnit test case with the mode of interaction that it should
 * implement with the Papyrus editor's profile migration dialog.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target({ ElementType.TYPE, ElementType.METHOD })
public @interface DialogInteractionMode {
	DialogInteractionKind value();
}
